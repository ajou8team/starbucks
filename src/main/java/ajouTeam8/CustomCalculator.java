package ajouTeam8;

public class CustomCalculator {
    public int add(int valueNum1, int valueNum2){
        return valueNum1 + valueNum2;
    }
    public int subtract(int valueNum1, int valueNum2){
        return valueNum1 - valueNum2;

    }

    public double divide(double valueNum1, double valueNum2){

        return valueNum1 / valueNum2;
    }


}
